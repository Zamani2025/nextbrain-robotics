    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $exc = Gallery::findOrFail($id);
        Storage::delete($exc->image);
        $exc->delete();
        toastr()->error("Gallery successfully deleted");
        return back();
    }
